/**
 * Helper function to get alt text by category and type
 * @param {string} category - The category (e.g., 'therapy', 'facilities')
 * @param {string} type - The specific type (e.g., 'individual', 'bedroom')
 * @returns {string} - The SEO-optimized alt text
 */
export const getAltText = (category, type) => {
  if (imageAltTexts[category] && imageAltTexts[category][type]) {
    return imageAltTexts[category][type];
  }
  return `Image from ${category} section at professional rehab facility`;
};

/**
 * Generate custom alt text with keyword injection
 * @param {string} description - Base description of the image
 * @param {string} keyword - Keyword to naturally integrate
 * @returns {string} - Generated alt text
 */
export const generateAltText = (description, keyword = null) => {
  const keywords = [
    'addiction therapy session',
    'counseling for substance abuse',
    'recovery environment',
    'professional rehab facility in India',
    'mental health support center'
  ];
  
  // If no keyword specified, use a random one
  const selectedKeyword = keyword || keywords[Math.floor(Math.random() * keywords.length)];
  
  // Ensure description doesn't already contain the keyword
  if (description.toLowerCase().includes(selectedKeyword.toLowerCase())) {
    return description;
  }
  
  // Add keyword naturally
  return `${description} at ${selectedKeyword}`;
};

/**
 * Get all alt texts for a specific category
 * @param {string} category - The category name
 * @returns {object} - All alt texts in that category
 */
export const getCategory = (category) => {
  return imageAltTexts[category] || {};
};

/**
 * Search for alt text by keyword
 * @param {string} searchTerm - Term to search for
 * @returns {array} - Array of matching alt texts
 */
export const searchAltText = (searchTerm) => {
  const results = [];
  const lowerSearch = searchTerm.toLowerCase();
  
  Object.keys(imageAltTexts).forEach(category => {
    Object.keys(imageAltTexts[category]).forEach(type => {
      const altText = imageAltTexts[category][type];
      if (altText.toLowerCase().includes(lowerSearch)) {
        results.push({
          category,
          type,
          altText
        });
      }
    });
  });
  
  return results;
};

